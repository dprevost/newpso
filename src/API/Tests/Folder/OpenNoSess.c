#include "Common/Common.h"
#include <photon/photon.h>
#include "API/Folder.h"
#include "API/Tests/quasar-run.h"

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void setup_test()
{
   assert( startQuasar() );
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void teardown_test()
{
   assert( stopQuasar() );
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

void test_pass( void ** state )
{
   PSO_HANDLE objHandle,  sessionHandle;
   PSO_HANDLE shmemHandle;
   int errcode;
   
   errcode = psoInit( "10701", "OpenNoSess" );
   assert_true( errcode == PSO_OK );
   
   errcode = psoInitSession( shmemHandle, &sessionHandle );
   assert_true( errcode == PSO_OK );

   errcode = psoCreateFolder( sessionHandle,
                              "/api_folder_open_nosess",
                              strlen("/api_folder_open_nosess") );
   assert_true( errcode == PSO_OK );
   //&& errcode != PSO_OBJECT_ALREADY_PRESENT ) {

   /* Close the session and try to act on the object */

   errcode = psoCommit( sessionHandle );
   assert_true( errcode == PSO_OK );
   
   errcode = psoExitSession( sessionHandle );
   assert_true( errcode == PSO_OK );

   /*
    * sessionHandle is a pointer to deallocated memory. We either get the
    * error or we crash!
    */
   errcode = psoFolderOpen( sessionHandle,
                            "/api_folder_op",
                            strlen("/api_folder_op"),
                            &objHandle );
   assert_true( errcode == PSO_WRONG_TYPE_HANDLE );

   psoExit();
}

/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */

int main()
{
   int rc = 0;
#if defined(PSO_UNIT_TESTS)
   const UnitTest tests[] = {
      unit_test_setup_teardown( test_pass, setup_test, teardown_test ),
   };

   rc = run_tests(tests);
   
#endif
   return rc;
}

